/*
*********************************************************************************************************
*
*	模块名称 : LCD 硬件接口移植层
*	文件名称 : bsp_lcd_port.c
*	说    明 : emWin 驱动所需的硬件接口函数实现。
*
*	本层职责 :
*		1. 对外提供与硬件无关的 LCD_Xxx() 函数名（供 GUIDRV_Template.c 调用）
*		2. 内部调用 NT35510_XxxGUI() 完成实际寄存器操作
*		3. 维护 g_LcdWidth / g_LcdHeight 全局变量
*
*	移植说明 :
*		若更换屏幕驱动芯片，只需修改本文件中各函数体内的底层调用，
*		GUIDRV_Template.c 保持不变。
*
*********************************************************************************************************
*/

#include "bsp_lcd_port.h"

/* ============================================================
 * 全局变量定义
 * 初始值来自 NT35510 宏定义，NT35510_Init() 完成后即有效
 * ============================================================ */
uint16_t g_LcdWidth  = NT35510_WIDTH;    /* 480 */
uint16_t g_LcdHeight = NT35510_HEIGHT;   /* 800 */

/* ============================================================
 * 屏幕信息查询
 * ============================================================ */

/*
*********************************************************************************************************
*	函 数 名: LCD_GetWidth
*	功能说明: 读取 LCD 分辨率之宽度
*	形    参: 无
*	返 回 值: LCD 宽度（像素）
*********************************************************************************************************
*/
uint16_t LCD_GetWidth(void)
{
    return g_LcdWidth;
}

/*
*********************************************************************************************************
*	函 数 名: LCD_GetHeight
*	功能说明: 读取 LCD 分辨率之高度
*	形    参: 无
*	返 回 值: LCD 高度（像素）
*********************************************************************************************************
*/
uint16_t LCD_GetHeight(void)
{
    return g_LcdHeight;
}

/* ============================================================
 * emWin 对接接口实现
 * ============================================================ */

/*
*********************************************************************************************************
*	函 数 名: BSP_LCD_PutPixelGUI
*	功能说明: 写单个像素。对应 GUIDRV_Template.c 中的 _SetPixelIndex。
*	          RA8875 例程对应函数: RA8875_PutPixelGUI()
*	形    参: x, y   - 物理坐标
*	          color  - RGB565 颜色
*	返 回 值: 无
*********************************************************************************************************
*/
void BSP_LCD_PutPixelGUI(uint16_t x, uint16_t y, uint16_t color)
{
    NT35510_PutPixelGUI(x, y, color);
}

/*
*********************************************************************************************************
*	函 数 名: BSP_LCD_GetPixelGUI
*	功能说明: 读单个像素。对应 GUIDRV_Template.c 中的 _GetPixelIndex。
*	          RA8875 例程对应函数: RA8875_GetPixelGUI()
*	形    参: x, y - 物理坐标
*	返 回 值: RGB565 颜色值
*********************************************************************************************************
*/
uint16_t BSP_LCD_GetPixelGUI(uint16_t x, uint16_t y)
{
    return NT35510_GetPixelGUI(x, y);
}

/*
*********************************************************************************************************
*	函 数 名: BSP_LCD_DrawHLine
*	功能说明: 画单色水平线。对应 GUIDRV_Template.c 中的 _DrawHLine 优化分支。
*	          RA8875 例程对应函数: RA8875_DrawHLine()
*	          注：高频调用路径，NT35510 用 SetWindow 连续写方式实现，效率高。
*	形    参: x0    - 起始 X 坐标
*	          y     - 行坐标
*	          x1    - 结束 X 坐标
*	          color - RGB565 颜色
*	返 回 值: 无
*********************************************************************************************************
*/
void BSP_LCD_DrawHLine(uint16_t x0, uint16_t y, uint16_t x1, uint16_t color)
{
    NT35510_DrawHLineGUI(x0, y, x1, color);
}

/*
*********************************************************************************************************
*	函 数 名: BSP_LCD_DrawVLine
*	功能说明: 画单色垂直线。对应 GUIDRV_Template.c 中的 _DrawVLine 优化分支。
*	          RA8875 例程对应函数: RA8875_DrawVLine()
*	形    参: x     - 列坐标
*	          y0    - 起始 Y 坐标
*	          y1    - 结束 Y 坐标
*	          color - RGB565 颜色
*	返 回 值: 无
*********************************************************************************************************
*/
void BSP_LCD_DrawVLine(uint16_t x, uint16_t y0, uint16_t y1, uint16_t color)
{
    NT35510_DrawVLineGUI(x, y0, y1, color);
}

/*
*********************************************************************************************************
*	函 数 名: BSP_LCD_FillRectGUI
*	功能说明: 填充矩形（端点坐标）。对应 GUIDRV_Template.c 中的 _FillRect 优化分支。
*	          RA8875 例程对应函数: RA8875_RTERect()
*	          注：emWin 传入的是端点坐标(x0,y0)-(x1,y1)，不是左上角+宽高。
*	形    参: x0, y0 - 左上角坐标
*	          x1, y1 - 右下角坐标
*	          color  - RGB565 颜色
*	返 回 值: 无
*********************************************************************************************************
*/
void BSP_LCD_FillRectGUI(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1, uint16_t color)
{
    NT35510_FillRectGUI(x0, y0, x1, y1, color);
}

/*
*********************************************************************************************************
*	函 数 名: BSP_LCD_DrawHColorLine
*	功能说明: 画彩色水平线，颜色来自像素数组。
*	          对应 GUIDRV_Template.c 中的 _DrawBitLine16BPP / _DrawBitLine32BPP 优化分支。
*	          RA8875 例程对应函数: RA8875_DrawHColorLine()
*	          注：这是位图和图片显示的核心路径，性能影响最大。
*	形    参: x      - 起始 X 坐标
*	          y      - 行坐标
*	          xsize  - 像素数量
*	          pColor - RGB565 颜色数组指针
*	返 回 值: 无
*********************************************************************************************************
*/
void BSP_LCD_DrawHColorLine(uint16_t x, uint16_t y, uint16_t xsize, const uint16_t *pColor)
{
    NT35510_DrawHColorLineGUI(x, y, xsize, pColor);
}